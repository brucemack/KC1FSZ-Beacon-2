void Driver_init() {

	si5351aInit(&ic21);
	// Set the transmit frequency
	encoder.setFreq(7050000);
	// Setup the message to be broadcast on the
	encoder.queueMessage(Msg);

	// Toggle
	for (int i = 0; i < 4; i++) {
		ind1.setState(true);
		HAL_Delay(500);
		ind1.setState(false);
		HAL_Delay(500);
	}
}

